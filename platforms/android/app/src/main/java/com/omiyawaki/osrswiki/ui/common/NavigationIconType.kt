package com.omiyawaki.osrswiki.ui.common

/**
 * Defines the types of navigation icons that can be displayed in the Toolbar.
 */
enum class NavigationIconType {
    BACK,       // Standard back arrow
    MENU,       // Hamburger icon for a navigation drawer (if you add one later)
    CLOSE,      // X icon for dialog-like screens or modal flows
    NONE        // No navigation icon
}
